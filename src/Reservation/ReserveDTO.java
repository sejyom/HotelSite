package Reservation;

public class ReserveDTO {
	private String resvId;
	private String resvStart;
	private String resvEnd;
	private String roomId;
	private String resvState;
	
	public String getResvId() {
		return resvId;
	}
	public void setResvId(String resvId) {
		this.resvId = resvId;
	}
	public String getResvStart() {
		return resvStart;
	}
	public void setResvStart(String resvStart) {
		this.resvStart = resvStart;
	}
	public String getResvEnd() {
		return resvEnd;
	}
	public void setResvEnd(String resvEnd) {
		this.resvEnd = resvEnd;
	}
	public String getRoomId() {
		return roomId;
	}
	public void setRoomId(String roomId) {
		this.roomId = roomId;
	}
	public String getResvState() {
		return resvState;
	}
	public void setResvState(String resvState) {
		this.resvState = resvState;
	}
	
	
}
